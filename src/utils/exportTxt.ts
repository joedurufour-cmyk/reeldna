import { AnalysisResult } from '../types';

export function exportTxt(result: AnalysisResult, originalText: string): string {
  const lines: string[] = [];
  lines.push('====================================');
  lines.push('  REELDNA ANALYSIS REPORT');
  lines.push('  Decode why content gets attention');
  lines.push('====================================');
  lines.push('');
  lines.push('ORIGINAL TEXT:');
  lines.push('------------------------------------');
  lines.push(originalText);
  lines.push('');
  lines.push('HOOK DETECTED:');
  lines.push(result.hook);
  lines.push('');
  lines.push(`HOOK SCORE: ${result.hookScore}/10`);
  lines.push(`HOOK TYPE: ${result.hookType}`);
  lines.push('');
  lines.push(`DOMINANT EMOTION: ${result.dominantEmotion}`);
  lines.push('');
  lines.push('NARRATIVE:');
  lines.push(result.narrative.join(' → '));
  lines.push('');
  lines.push('CTA:');
  lines.push(result.cta);
  lines.push('');
  lines.push(`VISUAL PATTERN: ${result.visualPattern}`);
  lines.push(`RETENTION PROBABILITY: ${result.retentionProbability}%`);
  lines.push('');
  lines.push('WHY IT WORKS:');
  result.whyItWorks.forEach((b) => lines.push(`• ${b}`));
  lines.push('');
  lines.push('ATTENTION KILLERS:');
  lines.push(result.attentionKillers.join(', '));
  lines.push('');
  lines.push('VIRAL DNA:');
  lines.push(`  Curiosity: ${result.viralDNA.curiosity}/10`);
  lines.push(`  Authority: ${result.viralDNA.authority}/10`);
  lines.push(`  Novelty:   ${result.viralDNA.novelty}/10`);
  lines.push(`  Urgency:   ${result.viralDNA.urgency}/10`);
  lines.push(`  Proof:     ${result.viralDNA.proof}/10`);
  lines.push(`  Clarity:   ${result.viralDNA.clarity}/10`);
  lines.push('');
  lines.push('STEAL THE FRAMEWORK:');
  lines.push('------------------------------------');
  lines.push(result.stealFramework);
  lines.push('');
  lines.push('SUMMARY:');
  lines.push(result.summary);
  lines.push('');
  lines.push('====================================');
  lines.push('Generated by ReelDNA | reeldna.app');
  lines.push('====================================');
  return lines.join('\n');
}
